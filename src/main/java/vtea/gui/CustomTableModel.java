package vtea.gui;

import javax.swing.table.AbstractTableModel;

/**
 *
 * @author sethwinfree
 */
public class CustomTableModel extends AbstractTableModel {

    private String[] columnNames = {"Type",
        "Proj", "Do"};
    private Object[][] data = {
        {"Kathy", new Boolean(true)},
        {"John", new Boolean(false)},
        {"Sue", new Boolean(false)},
        {"Jane", new Boolean(false)},
        {"Joe", new Boolean(false)}
    };

    public Object[][] getData() {
        return data;
    }

    public void setColumnNames(String[] columns) {
        columnNames = columns;
    }

    public void setData(Object[][] content) {
        data = content;
    }

    public int getColumnCount() {
        return columnNames.length;
    }

    public int getRowCount() {
        return data.length;
    }

    @Override
    public String getColumnName(int col) {
        return columnNames[col];
    }

    @Override
    public Object getValueAt(int row, int col) {
        return data[row][col];
    }

    /*
         * JTable uses this method to determine the default renderer/
         * editor for each cell.  If we didn't implement this method,
         * then the last column would contain text ("true"/"false"),
         * rather than a check box.
     */
    @Override
    public Class getColumnClass(int c) {
        if (c == 2) {
            return Boolean.class;
        }
        return getValueAt(0, c).getClass();
    }

    /*
         * Don't need to implement this method unless your table's
         * editable.
     */
    @Override
    public boolean isCellEditable(int row, int col) {
        //Note that the data/cell address is constant,
        //no matter where the cell appears onscreen.
        if (col < 2) {
            return false;
        } else {
            return true;
        }
    }

    /*
         * Don't need to implement this method unless your table's
         * data can change.
     */
    @Override
    public void setValueAt(Object value, int row, int col) {
        data[row][col] = value;
        fireTableCellUpdated(row, col);
    }

}
